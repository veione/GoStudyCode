func (h *IntHeap) Pop() interface{} {
	n := len(*h)
	x := (*h)[n-1]
	*h = (*h)[0 : n-1]
	return x
}

func main() {
	h := &IntHeap{3, 4, 1, 5, 10, 0}
	heap.Init(h)
	//fmt.Printf("top :%v", (*h)[0])
	for h.Len() > 0 {
		top := heap.Pop(h)
		fmt.Printf("%v ", top)
	}
}
